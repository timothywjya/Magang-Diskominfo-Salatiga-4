     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'password' => 'sometimes|nullable|confirmed'
        ]);
        $user = User::find($id);
        if ($request->password) $user->password = bcrypt($request->password);
        $user->save();
        return redirect()->route('login');
    }

